
function toggleblock() {
    var bloc =document.getElementById("monbloc");

    if (bloc.style.display==="none") {
        bloc.style.display ="block"; 
    } 
}





function toggle() {
    var bloc =document.getElementById("monbloc");

    if (bloc.style.display==="block") {
        bloc.style.display ="none"; 
    }

}





















